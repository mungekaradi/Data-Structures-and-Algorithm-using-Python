# string
x= "Hello World"
print(x)
print(type(x))

# integer
x= 33
print(x)
print(type(x))

# float(decimal)
x= 88.9
print(x)
print(type(x))

# complex
x= 2j
print(x)
print(type(x))

# list
x= ["Bmw","Audi","Volvo","Ferrari"]
print(x)
print(type(x))

# tuple
x= ("Dell","acer","hp")
print(x)
print(type(x))

# range
x= range(6)
print(x)
print(type(x))

# dictionary
x= {
    "name": "samit",
    "age": 18
    }
print(x)
print(type(x))

# set
x= {"Realme","apple","samsung"}
print(x)
print(type(x))

# frozenset
x= frozenset({"red","blue","green"})
print(x)
print(type(x))

# boolean
x= True
print(x)
print(type(x))

# bytes
x= b"hello"
print(x)
print(type(x))

# bytearray
x= bytearray(5)
print(x)
print(type(x))

# memoryview
x= memoryview (bytes(5))
print(x)
print(type(x))

# NoneType
x= None
print(x)
print(type(x))

